package com.craftsman.training.arrays;

public class SegundoArray {

	public static void main(String[] args) {
		String[] misNombres = { "Madrid", "Barcelona", "Sevilla", "Zaragoza" };

		for (int i = 0; i < misNombres.length; i++) {
			System.out.println("Mi nombre " + i + " : " + misNombres[i]);
		}
	}
}